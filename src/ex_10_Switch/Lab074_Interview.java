package ex_10_Switch;

public class Lab074_Interview {
    public static void main(String[] args) {
        char code = 'c';

        switch (code){
            default:
                System.out.println("Hello");
                break;
            case 65:
                System.out.println("A");
                break;
            case 66:
                System.out.println("B");
                break;

        }
    }

}
