package ex_05_TypeCasting;

public class Lab_TypeCasting_Tasks {

    public static void main(String[] args) {
        // Program 4
        int a = 100;
        double d = a;
        System.out.println(d);

        // Program 5
        double d2 = 100.854;
        int a1 = (int)d2;
        System.out.println(a1);

        // Program 6
        char c = 'A';
        int a2 = (int)c;
        System.out.println(a2);

        // Program 7
        int a3 = 10;
        int b = 3;
        float result = (float)a3/(float)b;
        System.out.println(result);


    }
}
