package ex_04_Operators;

public class Lab023_Unary_Operator {
    public static void main(String[] args) {
        // Unary Operator
        int a = +10;
        // "+" is optional for positive value
        System.out.println(a);

        int b = -110;
        int result = a+b;

        System.out.println(b);

        System.out.println(result
        );
    }
}
