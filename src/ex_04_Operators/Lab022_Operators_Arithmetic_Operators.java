package ex_04_Operators;

public class Lab022_Operators_Arithmetic_Operators {
    public static void main(String[] args) {

        int a = 20;
        int b = 3;
        float c = 3.3f;


        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);
        System.out.println(b+c);

    }

}
