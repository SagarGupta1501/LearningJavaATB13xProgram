package ex_08_Increment_Decrement;

public class Lab059_Advance_ID {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++ + a);
        // A + B
        // A -> a++ -> ExpA
        // B -> a -> ExpB

        // Line no. | a | Exp
        // 6 | 10 | NA
        // 7 | 11 | 10 + 11 -> result = 21
    }
}
