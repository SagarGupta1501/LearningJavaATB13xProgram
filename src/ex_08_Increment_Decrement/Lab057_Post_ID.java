package ex_08_Increment_Decrement;

public class Lab057_Post_ID {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(++a);
        System.out.println(a);

        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);
    }
}
